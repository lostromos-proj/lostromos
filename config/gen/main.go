// gen generates config/config_gen.go from the flagSpec slice below.
// Run via: go generate ./config
package main

import (
	"bytes"
	"go/format"
	"log"
	"os"
	"text/template"
)

type flagSpec struct {
	Field   string // Config struct field name
	Flag    string // CLI flag name (without --)
	Env     string // Environment variable name
	Type    string // "bool" | "string" | "int"
	Default string // Go expression for env-helper default arg (empty for bool)
	HelpDef string // Go expression for the help table's def column
	Desc    string // Help description
}

var specs = []flagSpec{
	{"DisableConstraintInput", "disable-constraint-input", "DISABLE_CONSTRAINT_INPUT", "bool", "", `"false"`, "Disable the input that watches constraint objects"},
	{"Verbose", "verbose", "VERBOSE", "bool", "", `"false"`, "Enable verbose logging"},
	{"Kubeconfig", "kubeconfig", "KUBECONFIG", "string", `""`, `"(none \u2014 in-cluster)"`, "Path to a kubeconfig file; omit to use in-cluster config"},
	{"MetricsHost", "metrics-host", "METRICS_HOST", "string", "DefaultMetricsHost", "DefaultMetricsHost", "Interface to bind for the metrics HTTP endpoint"},
	{"MetricsPort", "metrics-port", "METRICS_PORT", "int", "DefaultMetricsPort", `"9090"`, "Port to bind for the metrics HTTP endpoint"},
	{"MetricsPath", "metrics-path", "METRICS_PATH", "string", "DefaultMetricsPath", "DefaultMetricsPath", "URL path for serving metrics"},
}

const tmpl = `// Code generated by go generate; DO NOT EDIT.

package config

import (
	"flag"
)

// Config holds all runtime configuration for lostromos.
type Config struct {
{{- range .}}
	{{.Field}} {{goType .Type}}
{{- end}}
}

// newFlagSet returns a FlagSet with all flags bound to cfg's fields.
func newFlagSet(cfg *Config) *flag.FlagSet {
	fs := flag.NewFlagSet("lostromos", flag.ContinueOnError)
	fs.Usage = func() { printHelp() }
{{range .}}	fs.{{flagFunc .Type}}(&cfg.{{.Field}}, "{{.Flag}}", {{defaultCall .}}, "{{.Desc}}")
{{end}}	return fs
}

// helpEntries is used by printHelp to render the usage table.
var helpEntries = []struct{ flag, env, def, desc string }{
{{- range .}}
	{"--{{.Flag}}", "{{.Env}}", {{.HelpDef}}, "{{.Desc}}"},
{{- end}}
	{"--help", "", "", "Print this help message and exit"},
}
`

func goType(t string) string {
	switch t {
	case "bool":
		return "bool"
	case "string":
		return "string"
	case "int":
		return "int"
	}
	panic("unknown type: " + t)
}

func flagFunc(t string) string {
	switch t {
	case "bool":
		return "BoolVar"
	case "string":
		return "StringVar"
	case "int":
		return "IntVar"
	}
	panic("unknown type: " + t)
}

func defaultCall(f flagSpec) string {
	switch f.Type {
	case "bool":
		return `boolEnv("` + f.Env + `")`
	case "string":
		return `strEnv("` + f.Env + `", ` + f.Default + `)`
	case "int":
		return `intEnv("` + f.Env + `", ` + f.Default + `)`
	}
	panic("unknown type: " + f.Type)
}

func main() {
	funcMap := template.FuncMap{
		"goType":      goType,
		"flagFunc":    flagFunc,
		"defaultCall": defaultCall,
	}
	t := template.Must(template.New("config_gen").Funcs(funcMap).Parse(tmpl))

	var buf bytes.Buffer
	if err := t.Execute(&buf, specs); err != nil {
		log.Fatal(err)
	}

	src, err := format.Source(buf.Bytes())
	if err != nil {
		log.Fatalf("format: %v\n---\n%s", err, buf.Bytes())
	}

	if err := os.WriteFile("config_gen.go", src, 0644); err != nil {
		log.Fatal(err)
	}
}
